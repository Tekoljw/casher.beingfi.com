// 后端返回的翻译数据结构
interface LanguageConfigResponse {
  code: number;
  msg: string;
  data: {
    config: string | Record<string, TranslationDict>; // config 可能是 JSON 字符串或对象，支持任意语言代码
  };
}

// 创建和使用上下文的逻辑
const LanguageContext = createContext<LanguageContextType | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    // 从localStorage读取语言设置
    const savedLanguage = localStorage.getItem('language');
    // 默认使用中文，如果保存的语言是 'en' 则使用英文，否则使用保存的语言或中文
    return savedLanguage || 'zh';
  });
  
  // 翻译字典状态，初始使用默认翻译
  const [translations, setTranslations] = useState<Translations>(defaultTranslations);
  const [isLoading, setIsLoading] = useState(true);
  
  // 从后端加载翻译数据
  useEffect(() => {
    const loadTranslations = async () => {
      try {
        setIsLoading(true);
        
        // 调用接口获取翻译配置（所有页面都需要调用，包括登录页面）
        // 注意：即使没有 token 也尝试调用，因为翻译配置可能是公开的
        console.log('Calling getLangConfig API...', 'Current path:', window.location.pathname);
        const response = await apiRequest<LanguageConfigResponse>('POST', '/Api/Index/getLangConfig');
        
        console.log('getLangConfig response:', response);
        
        if (response.code === 0 && response.data?.config) {
          try {
            // 判断 config 是字符串还是对象
            let configData: Record<string, TranslationDict>;
            
            if (typeof response.data.config === 'string') {
              // 如果是字符串，需要解析
              configData = JSON.parse(response.data.config) as Record<string, TranslationDict>;
            } else {
              // 如果已经是对象，直接使用
              configData = response.data.config as Record<string, TranslationDict>;
            }
            
            console.log('Parsed configData languages:', Object.keys(configData));
            
            // 后端数据优先，覆盖默认翻译
            // 先使用默认翻译作为基础，然后用后端数据覆盖（后端数据优先级更高）
            // 支持动态语言：合并所有语言，包括后端返回的新语言
            const mergedTranslations: Translations = { ...defaultTranslations };
            
            // 合并后端返回的所有语言数据
            Object.keys(configData).forEach(langCode => {
              mergedTranslations[langCode] = {
                ...(defaultTranslations[langCode] || {}),
                ...(configData[langCode] || {})
              };
            });
            
            setTranslations(mergedTranslations);
            
            console.log('Language config loaded successfully', {
              configType: typeof response.data.config,
              languagesFromConfig: Object.keys(configData),
              totalLanguages: Object.keys(mergedTranslations).length,
              zhKeys: Object.keys(configData.zh || {}).length,
              enKeys: Object.keys(configData.en || {}).length,
              totalZhKeys: Object.keys(mergedTranslations.zh).length,
              totalEnKeys: Object.keys(mergedTranslations.en).length
            });
            
            // 打印合并后的 translations 数据
            console.log('Merged translations languages:', Object.keys(mergedTranslations));
          } catch (parseError) {
            console.error('Failed to parse config:', parseError, 'config value:', response.data?.config);
            // JSON 解析失败，使用默认翻译
            setTranslations(defaultTranslations);
          }
        } else {
          // 如果加载失败，使用默认翻译
          console.warn('Failed to load language config, using default translations', response);
          setTranslations(defaultTranslations);
        }
      } catch (error: any) {
        console.error('Error loading language config:', error);
        // 如果是未登录错误，不进行重定向，直接使用默认翻译
        if (error.message?.includes('未知用户') || error.message?.includes('401')) {
          console.log('Unauthorized, using default translations');
          setTranslations(defaultTranslations);
        } else {
          // 其他错误也使用默认翻译
          setTranslations(defaultTranslations);
        }
      } finally {
        setIsLoading(false);
      }
    };
    
    loadTranslations();
  }, []);
  
  // 设置语言函数，并存储到localStorage
  const setLanguage = (lang: string) => {
    // 如果该语言在 translations 中存在，则设置；否则回退到 zh 或 en
    if (translations[lang]) {
    setLanguageState(lang);
    localStorage.setItem('language', lang);
    } else {
      // 如果语言不存在，回退到默认语言
      const fallbackLang = translations['zh'] ? 'zh' : translations['en'] ? 'en' : Object.keys(translations)[0] || 'zh';
      setLanguageState(fallbackLang);
      localStorage.setItem('language', fallbackLang);
    }
  };
  
  // 更新翻译字典的函数
  const updateTranslations = (newTranslations: Translations) => {
    setTranslations(newTranslations);
    // 如果当前语言不在新的翻译字典中，回退到默认语言
    if (!newTranslations[language]) {
      const fallbackLang = newTranslations['zh'] ? 'zh' : newTranslations['en'] ? 'en' : Object.keys(newTranslations)[0] || 'zh';
      setLanguageState(fallbackLang);
      localStorage.setItem('language', fallbackLang);
    }
  };
  
  // 翻译函数，获取指定语言的翻译文本
  const t = (key: string, fallback?: string): string => {
    // 如果当前语言存在且该键存在，返回翻译
    if (translations[language] && translations[language][key]) {
      return translations[language][key];
    }
    // 如果当前语言不存在该键，尝试从中文或英文获取
    if (translations['zh'] && translations['zh'][key]) {
      return translations['zh'][key];
    }
    if (translations['en'] && translations['en'][key]) {
      return translations['en'][key];
    }
    // 如果都不存在，返回 fallback 或 key
    return fallback || key;
  };
  
  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, updateTranslations, translations }}>
      {children}
    </LanguageContext.Provider>
  );
}

// 使用语言上下文的钩子
export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    // 提供更详细的错误信息，帮助调试
    const error = new Error('useLanguage必须在LanguageProvider内部使用');
    console.error('LanguageContext错误:', {
      context,
      stack: error.stack,
      timestamp: new Date().toISOString()
    });
    throw error;
  }
  return context;
}